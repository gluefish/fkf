      <td>George Franklin Forbes was born June 26, 1915 in Boston, Massachusetts.
        His parents were Mark Ahza Forbes, a carpenter, and Leau Marie Boudreau.
        He was an only child. He married Mary Eleanor Kimball on March 9, 1942.
        They had ten children; seven girls and three boys. He worked in the aero-space
        industry writing computer guidance software programs for rockets, missiles,
        airplanes, etc. He was known as an industrial mathematician. (You will
        find all six pages of his resume below. Click on the thumbnail to see
        full size versions of the pages.) He was an officer in the Naval Reserves
        for most of his life. He retired as a Lieutenant Commander. He had two
        works published: One was a short story &quot;Doorways
        to Infinity.&quot; published in the December, 1959 issue of <u>Future
        Science Fiction,</u>
        The other was a technical manual entitled &quot;Digital Differential
        Analyzers.&quot;
        He was interested in cave exploring, and belonged to the Speleological
        Society and sometimes went on cave exploring trips. He belonged to the
        British Interplanetary Society and in the 1950's, before the space program
        got going, wrote articles for them about rocket space trajectories. He
        was also interested in UFO's. He rode a full dress Harley-Davidson motorcycle
        to work every day. He accumulated over a million miles on the motorcycle.
        He would take us (individually) to Disneyland for our birthday on the
        motorcycle, a trip of about fifty miles. It was <strong><em>so</em></strong> cool! </td>
